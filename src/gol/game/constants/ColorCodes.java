package gol.game.constants;

/** String constant values for the ansi color codes. */
public class ColorCodes {
  /** String ansi color code for the white color. */
  public static final String WHITE_COLOR = "\u001B[1;97m";
  /** String ansi color code for the red color. */
  public static final String RED_COLOR = "\033[1;31m";
  /** String ansi color code for the cyan color. */
  public static final String CYAN_COLOR = "\033[1;36m";
  /** String ansi color code for the yellow color. */
  public static final String YELLOW_COLOR = "\033[1;33m";
}
